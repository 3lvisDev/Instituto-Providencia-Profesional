############ INICIO DEL PROGRAMA ############
numero1 = float(input("Ingrese el primer número: "))  # solicitud al usuario el primer número
numero2 = float(input("Ingrese el segundo número: "))  # solicitud al usuario el segundo número
numero3 = float(input("Ingrese el tercer número: "))  # solicitud al usuario el tercer número

############ PROMEDIO ############
promedio = (numero1 + numero2 + numero3) / 3  # calculacion el promedio de los tres números

############ RESULTADO ############
print("El promedio de", numero1, ",", numero2, "y", numero3, "es:", promedio)
